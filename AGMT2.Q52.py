#How Many Basic Types Of Functions Are Available In Python?
'''In Python, there are three basic types of functions:

Built-in functions: These functions are pre-defined in Python and are always
available for use without requiring an explicit import. Examples include print(), len(), range(), etc.

User-defined functions: These functions are defined by the user to perform specific tasks.
Users can create their own functions using the def keyword followed by the function name, parameters, and a block of code.

Anonymous functions (lambda functions): These functions are defined using the lambda keyword and are not bound to a name.
Lambda functions are typically used for short, one-line operations. '''
